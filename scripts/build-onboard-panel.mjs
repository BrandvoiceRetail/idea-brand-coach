/**
 * Bundle the onboarding-panel MCP App client into a self-contained IIFE string.
 *
 * The host loads the `ui://brand-coach/onboard` resource as a single sandboxed HTML document,
 * so the @modelcontextprotocol/ext-apps `App` client must be inlined (no runtime imports). This
 * esbuilds `src/mcp/panel/onboardPanelClient.ts` (which pulls in the SDK + zod) to a minified
 * IIFE and writes it as a TS string export the server inlines into the panel HTML.
 *
 * Regenerate after editing the client entry: `npm run build:panel`.
 */
import { build } from 'esbuild';
import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const entry = resolve(root, 'src/mcp/panel/onboardPanelClient.ts');
const outFile = resolve(root, 'src/mcp/panel/onboardPanelClient.generated.ts');

const result = await build({
  entryPoints: [entry],
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: ['es2020'],
  minify: true,
  write: false,
  legalComments: 'none',
});

// The bundle is inlined into an HTML <script> tag; neutralize any literal
// "</script" so the HTML parser can't terminate the tag early. "<\/script" is
// identical to "</script" once the JS engine parses the string.
const js = result.outputFiles[0].text.replace(/<\/script/gi, '<\\/script');
const banner =
  '/* AUTO-GENERATED by scripts/build-onboard-panel.mjs — do not edit by hand.\n' +
  ' * Source: src/mcp/panel/onboardPanelClient.ts (bundled @modelcontextprotocol/ext-apps App client).\n' +
  ' * Regenerate: npm run build:panel */\n';

writeFileSync(outFile, `${banner}export const ONBOARD_PANEL_CLIENT_JS: string = ${JSON.stringify(js)};\n`);
console.log(`Wrote ${outFile} (${js.length} bytes of bundled JS)`);
