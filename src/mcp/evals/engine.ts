/**
 * Eval engine — assembles the full report from the configs, the corpus, and the metrics.
 * Pure and deterministic: same inputs → byte-identical report (no clock, no network).
 */
import { CONFIGS, CURRENT_CONFIG_ID } from './configs.js';
import { loadCorpus } from './corpus.js';
import {
  scoreConfig,
  buildCoachValue,
  buildCorrectnessKpis,
  buildGuardrails,
  buildFlags,
  corpusSummary,
} from './metrics.js';
import { liveTierStatus } from './liveTier.js';
import type { EvalReport } from './types.js';

export function buildReport(): EvalReport {
  const corpus = loadCorpus();
  const configs = CONFIGS.map((c) => scoreConfig(c, corpus));
  // Corpus-derived value KPIs + deterministic case-derived correctness KPIs (trigger/anchor/loop).
  const coachValue = [...buildCoachValue(corpus), ...buildCorrectnessKpis()];
  const coachValueScore = coachValue.length
    ? coachValue.reduce((a, k) => a + k.value, 0) / coachValue.length
    : 0;

  return {
    schemaVersion: 1,
    generatedNote:
      'Generated by `npm run evals` (src/mcp/evals/run.ts). Deterministic — re-run to refresh; no timestamp so an unchanged run is a no-op diff.',
    corpus: corpusSummary(corpus),
    configs,
    currentConfigId: CURRENT_CONFIG_ID,
    coachValue,
    coachValueScore,
    guardrails: buildGuardrails(),
    flags: buildFlags(corpus),
    liveTier: liveTierStatus(),
  };
}
