/**
 * Eval runner (CLI) — `npm run evals`.
 *
 * Builds the deterministic report and writes it as a typed `.ts` literal the admin
 * dashboard imports (a `.ts`, not `.json`, so the SPA type-checks it without enabling
 * resolveJsonModule in the app tsconfig). Prints a console summary. No timestamp, so an
 * unchanged run produces an identical file (clean git diffs).
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildReport } from './engine.js';
import { runLiveTier } from './liveTier.js';

const here = dirname(fileURLToPath(import.meta.url));
const outDir = resolve(here, '../../admin/coachEvals');
const outFile = resolve(outDir, 'report.generated.ts');

const report = buildReport();
// A1 (deterministic) server-contract checks run against the real in-process server and are
// baked into the committed report; A2 (LLM) stays gated inside runLiveTier.
report.liveTier = await runLiveTier();

const header = `/* eslint-disable */
// @generated by \`npm run evals\` (src/mcp/evals/run.ts). DO NOT EDIT BY HAND.
// The MCP evals suite scores and compares coach configurations (skill set × tool grounding)
// and the current coach's user-value KPIs. Re-run \`npm run evals\` to refresh.
import type { EvalReport } from '@/mcp/evals/types';

export const EVAL_REPORT: EvalReport = `;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, `${header}${JSON.stringify(report, null, 2)};\n`, 'utf8');

// ── console summary ──────────────────────────────────────────────────────────
const line = '─'.repeat(64);
console.log(line);
console.log('IDEA Brand Coach — MCP Evals');
console.log(line);
console.log(`Corpus: ${report.corpus.fixtures} fixtures · ${report.corpus.journeys} journeys`);
console.log(`Coach value score: ${(report.coachValueScore * 100).toFixed(0)}%`);
console.log('\nConfiguration comparison:');
for (const c of report.configs) {
  const mark = c.current ? ' (current)' : '';
  console.log(`  [${c.grade}] ${(c.composite * 100).toFixed(0)}%  ${c.label}${mark}`);
}
console.log('\nGuardrails:');
for (const g of report.guardrails) console.log(`  ${g.passed ? 'PASS' : 'FAIL'}  ${g.label}`);
console.log(`\nLive tier (A2 LLM): ${report.liveTier.available ? 'available' : 'gated'}`);
for (const m of report.liveTier.metrics ?? []) console.log(`  ${m.display.padStart(9)}  ${m.label}`);
if (report.flags.length) {
  console.log('\nFlags:');
  for (const f of report.flags) console.log(`  • ${f}`);
}
console.log(`\nWrote ${outFile}`);
console.log(line);
